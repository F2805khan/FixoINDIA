import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = join(dirname(fileURLToPath(import.meta.url)), "..");
const manifestPath = join(rootDir, "app.version.json");
const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));

const appVersion = String(manifest.appVersion || "1.0.0").trim();
const apiVersion = String(manifest.apiVersion || "1").trim();
const minClientVersion = String(manifest.minClientVersion || "1.0.0").trim();
const serviceName = String(manifest.serviceName || "fixoindia").trim();
const buildId =
  process.env.VERCEL_GIT_COMMIT_SHA?.slice(0, 12) ||
  process.env.GITHUB_SHA?.slice(0, 12) ||
  process.env.BUILD_ID ||
  new Date().toISOString();

const backendOut = `/* Auto-generated by scripts/sync-app-version.mjs — do not edit manually. */
export const APP_VERSION = ${JSON.stringify(appVersion)};
export const API_VERSION = ${JSON.stringify(apiVersion)};
export const BUILD_ID = ${JSON.stringify(buildId)};
export const MIN_CLIENT_VERSION = ${JSON.stringify(minClientVersion)};
export const SERVICE_NAME = ${JSON.stringify(serviceName)};

export const getVersionPayload = () => ({
  appVersion: APP_VERSION,
  apiVersion: API_VERSION,
  buildId: BUILD_ID,
  minClientVersion: MIN_CLIENT_VERSION,
  service: SERVICE_NAME
});
`;

const frontendOut = `/* Auto-generated by scripts/sync-app-version.mjs — do not edit manually. */
export const CLIENT_APP_VERSION = ${JSON.stringify(appVersion)};
export const CLIENT_API_VERSION = ${JSON.stringify(apiVersion)};
export const CLIENT_BUILD_ID = ${JSON.stringify(buildId)};
export const CLIENT_MIN_VERSION = ${JSON.stringify(minClientVersion)};
export const CLIENT_SERVICE_NAME = ${JSON.stringify(serviceName)};
`;

writeFileSync(join(rootDir, "backend/utils/appVersion.js"), backendOut, "utf8");
mkdirSync(join(rootDir, "frontend/src/generated"), { recursive: true });
writeFileSync(join(rootDir, "frontend/src/generated/appVersion.js"), frontendOut, "utf8");

for (const pkgPath of ["package.json", "backend/package.json", "frontend/package.json"]) {
  const fullPath = join(rootDir, pkgPath);
  const pkg = JSON.parse(readFileSync(fullPath, "utf8"));
  pkg.version = appVersion;
  writeFileSync(fullPath, `${JSON.stringify(pkg, null, 2)}\n`, "utf8");
}

console.log(`Synced app version ${appVersion} (api ${apiVersion}, build ${buildId})`);
